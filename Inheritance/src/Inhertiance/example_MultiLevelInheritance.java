package Inhertiance;

public class example_MultiLevelInheritance
{
	public static void main(String []args)
	{
		InstagramV3 i=new InstagramV3();
		System.out.println("...UPDATED VERSION OF INSTAGRAM.......");
		System.out.println("1)");
		i.post();
		System.out.println("2)");
		i.Filters();
		System.out.println("3)");
		i.Story();
		System.out.println("4)");
		i.reels();
	}
}
